static uint16_t crc16(uint8_t *buffer, uint16_t buffer_length)
{
    uint8_t crc_hi = 0xFF; /* high CRC byte initialized */
    uint8_t crc_lo = 0xFF; /* low CRC byte initialized */
    unsigned int i; /* will index into CRC lookup */

    /* pass through message buffer */
    while (buffer_length--) {
        i = crc_hi ^ *buffer++; /* calculate the CRC  */
        crc_hi = crc_lo ^ table_crc_hi[i];
        crc_lo = table_crc_lo[i];
    }

    return (crc_hi << 8 | crc_lo);
}

static unsigned byte_time;

extern void cfmakeraw(struct termios *);

static int verbose = 0;
static int rs485 = 0;
static int crc_check = 0;

static void makeraw(int fd) {
  struct termios t;
  check(ioctl(fd, TCGETS, &t), "TCGETS");
  cfmakeraw(&t);
  check(ioctl(fd, TCSETS, &t), "TCSETS");
}

static int serialsetup(const char *name, int speed) {
  int fd = open(name, O_RDWR);
  check(fd < 0, "open");
  setspeed(fd, speed);
  makeraw(fd);
  return fd;
}

static int socketsetup(int port) {
  int listening = socket(AF_INET6, SOCK_STREAM, 0);
  check(listening < 0, "socket");
  struct sockaddr_in6 addr;
  memset(&addr, 0, sizeof addr);
  addr.sin6_family = AF_INET6;
  addr.sin6_port = htons(port);
  int one = 1;
  check(setsockopt(listening, SOL_SOCKET, SO_REUSEADDR, &one, sizeof one), "SO_REUSEADDR");
  check(bind(listening, (const struct sockaddr *)&addr, sizeof addr), "bind");
  check(listen(listening, 5), "listen");
  return listening;
}

static void dump16(char prefix, const char *buffer, int length) {
  fprintf(stderr, "%c", prefix);
  for (int i = 0; i < length; i++)
    fprintf(stderr," %02x", (uint8_t) buffer[i]);
  for (int i = length; i < 16; i++)
    fprintf(stderr, "   ");
  fprintf(stderr, " %c ", prefix);
  for (int i = 0; i < length; i++) {
    uint8_t c = buffer[i];
    fprintf(stderr, "%c", c >= 32 && c <= 127 ? c : '.');
  }
  fprintf(stderr, "\n");
}

static void dump(char prefix, const char *buffer, int length) {
  for (; length >= 0; length -= 16, buffer += 16)
    dump16(prefix, buffer, length > 16 ? 16 : length);
}

static int relay(int from, int to, char prefix) {
  static char buffer[1024];
  int n = read(from, buffer, sizeof buffer);
  if (n > 0) {
    if (rs485 && prefix == '>')
      ioctl_rts(to, 0);
    write(to, buffer, n);
    if (rs485 && prefix == '>')
    {
      usleep(byte_time * n);
      ioctl_rts(to, 1);
    }

    if (verbose)
      dump(prefix, buffer, n);
    
    if (crc_check)
    {
      static char prev_prefix = '<';
      static char prevbuf[1024];
      static int prev_n = 0;

      if (prev_prefix == prefix && prefix == '>')
      {
        fprintf(stderr, "No reply to previous packet HASS->MODBUS:\n");
        dump(prefix, prevbuf, prev_n);
      }

      if (prefix == '>')
      {
        memcpy(prevbuf, buffer, n);
        prev_n = n;
      }


      prev_prefix = prefix;
      if (n >= 4)
      {
        uint16_t recv_crc = (buffer[n-2] << 8 | buffer[n-1]);
        uint16_t crc = crc16((uint8_t *)buffer, n-2);
        if (crc != recv_crc)
        {
          fprintf(stderr, "CRC ERROR: recv[%04X] computed[%04X]\n", recv_crc, crc);
          dump(prefix, buffer, n);
        }
      }
      else
      {
        fprintf(stderr, "Short packet!\n");
        dump(prefix, buffer, n);
      }
    }
  }
  return n;
}

static void bridge(int sockfd, int serfd) {
  struct pollfd fds[] = {
    { sockfd, POLLIN, 0 },
    { serfd, POLLIN, 0 }
  };
  for (;;) {
    poll(fds, 2, -1);
    if (fds[0].revents & POLLIN)
      if (relay(sockfd, serfd, '>') <= 0)
	return;
    if (fds[1].revents & POLLIN)
      if (relay(serfd, sockfd, '<') <= 0) {
	fprintf(stderr, "Serial port closed\n");
	exit(0);
      }
  }
}

__attribute__((noreturn))
static void usage(int error, const char *progname) {
  fprintf(error ? stderr : stdout, "Usage: %s device speed port\n", progname);
  exit(error);
}

int main(int argc, char * const argv[]) {
  int opt;
  while ((opt = getopt(argc, argv, "hrvc")) != -1)
    switch(opt) {
    case 'v':
      verbose = 1;
      break;
    case 'r':
      rs485 = 1;
      break;
    case 'c':
      crc_check = 1;
      break;
    case 'h':
      usage(0, argv[0]);
    default:
      usage(1, argv[0]);
    }
  if (argc - optind != 3)
    usage(1, argv[0]);
  int speed = atoi(argv[optind + 1]);
  int port = atoi(argv[optind + 2]);
  if (speed == 0 || port == 0)
    usage(1, argv[0]);
  int serial = serialsetup(argv[optind], speed);
  int listening = socketsetup(port);
  byte_time = (10000000 + speed / 2) / speed;
  for (;;) {
    if (verbose)
      printf("Waiting for incoming connection\n");
    int accepted = accept(listening, NULL, NULL);
    if (verbose)
      printf("Connection accepted\n");
    bridge(accepted, serial);
    close(accepted);
    if (verbose)
      printf("Connection closed\n");
  }
  exit(0);
}
